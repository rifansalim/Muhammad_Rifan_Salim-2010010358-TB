    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
if (txtkode.getText().equals("") || txtnama.getText().equals("") || txtjumlah.getText().equals("") || txttanggal.getText().equals("") || txtkondisi.getText().equals("") ){
            JOptionPane.showMessageDialog(null, "Field Tidak Boleh Kosong");
        } else {
            try {
               int s;
               String sql = "INSERT INTO barang_masuk VALUES ('"+txtkode.getText()+"', '"+txtnama.getText()+"', '"+txtjumlah.getText()+"', '"+txttanggal.getText()+"', '"+txtkondisi.getText()+"')";
               st = conn.createStatement();
               s = st.executeUpdate(sql);
               if (s == 1){
                   JOptionPane.showMessageDialog(null, "Sukses");
                   clear();
                   tampilan();
               }
           } catch (Exception e){
                   System.out.println(e.toString());
           }    
        }    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        int c = JOptionPane.showConfirmDialog(null, "Ingin Mengubah Data?","Informasi", JOptionPane.YES_NO_OPTION);
        if (c == JOptionPane.YES_OPTION){
            if (txtkode.getText().equals("") || txtnama.getText().equals("") || txtjumlah.getText().equals("") || txttanggal.getText().equals("") || txtkondisi.getText().equals("") ){
                JOptionPane.showMessageDialog(null, "Field Tidak Boleh Kosong");
            } else {
                try {
                    int s;
                    String sql = "UPDATE barang_masuk SET nama_barang = '"+txtnama.getText()+"', jumlah = '"+txtjumlah.getText()+"', tanggal_masuk = '"+txttanggal.getText()+"', kondisi = '"+txtkondisi.getText()+"' WHERE kode_barang = '"+txtkode.getText()+"' ";
                    st = conn.createStatement();
                    s = st.executeUpdate(sql);
                    if (s == 1){
                        JOptionPane.showMessageDialog(null, "Sukses");
                        clear();
                        tampilan();
                    }
                } catch (Exception e){
                        System.out.println(e.toString());
                } 
            }
        }
    }//GEN-LAST:event_btnUbahActionPerformed

