     public void tampilan(){
        Object [] baris = {"kode_barang", "nama_barang", "jumlah", "tanggal_masuk","kondisi"};
        tabMode = new DefaultTableModel(null, baris);
        tbmasuk.setModel(tabMode);
        try {
                String sql = "SELECT * FROM barang_masuk";
                st = conn.createStatement();
                rs = st.executeQuery(sql);
                while (rs.next()){
                        String kb = rs.getString("kode_barang");
                        String nb = rs.getString("nama_barang");
                        String jm = rs.getString("jumlah");
                        String tm = rs.getString("tanggal_masuk");
                        String ks = rs.getString("kondisi");

                        Object [] data = {kb,nb,jm,tm,ks};
                        tabMode.addRow(data);
                }
        } catch (Exception e){
                System.out.println(e.toString());
        }
    }
     
